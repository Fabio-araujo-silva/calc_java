public class contas {
  
 static void soma(int a,int b){
   int c = a+b; 
   System.out.println("soma é "+c);
  }
  static void subtracao(int a,int b){
   int c = a-b; 
   System.out.println("subtracao é "+c);
  }
  static void multiplicacao(int a,int b){
   int c = a*b; 
   System.out.println("multiplicacao é "+c);
  }
  static void divisao(double a,double b){
   double c = a/b; 
   System.out.println("divisao é "+c);
  }
  static void exponenciacao(double a,double b){
   double c = Math.pow(a,b); 
   System.out.println("exponenciação é "+c);
  }
  
}
